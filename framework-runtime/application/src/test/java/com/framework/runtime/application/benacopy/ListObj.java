package com.framework.runtime.application.benacopy;

public class ListObj {
	private String s;
	private int i;

	public ListObj() {
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}
	
	public String toString() {
		return i +" " + s;
	}
	
	

}
